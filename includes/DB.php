    public static function table(): string {
        global $wpdb;
        return $wpdb->prefix . 'mhuam_sessions';
    }


    /**
     * Returns a quoted SQL identifier for plugin-controlled table and column names.
     *
     * WordPress placeholders such as %s must not be used for SQL identifiers like
     * table names, column names or ORDER BY columns. This helper is only used with
     * identifiers generated by the plugin itself or with values already restricted
     * by valid_columns(). It strips every character except ASCII letters, numbers
     * and underscores, then wraps the result in backticks.
     *
     * Do not pass visitor input, request data or unsanitized option values here.
     */
    private static function sql_identifier(string $identifier): string {
        return '`' . preg_replace('/[^A-Za-z0-9_]/', '', $identifier) . '`';
    }

    private static function table_sql(?string $table = null): string {
        return self::sql_identifier($table ?: self::table());
    }

    private static function column_sql(array $columns): string {
        $allowed = self::valid_columns();
        $out = [];
        foreach ($columns as $column) {
            if (in_array($column, $allowed, true)) {
                $out[] = self::sql_identifier($column);
            }
        }
        return implode(', ', $out);
    }

    private static function valid_columns(): array {
        return [
            'session_id','user_id','display_name','user_login','roles','visitor_type','ip_address','ip_display','ip_hash',
            'current_url','page_type','attack_flags_json','page_history_json','referrer','user_agent','is_bot','bot_name',
            'bot_category','bot_risk','cart_json','cart_count','first_seen','last_seen','last_seen_ts','hits','last_ping_source'
        ];
    }

