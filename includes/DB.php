            KEY page_type (page_type)
        ) {$charset};";
        dbDelta($sql);
    }

    public static function maybe_upgrade(): void {
        if (get_option(self::DB_VERSION_OPTION) === self::DB_VERSION) {
            return;
        }

        $lock_key = 'mhuam_db_upgrade_lock';
        $lock_time = time();
        $lock_acquired = add_option($lock_key, (string)$lock_time, '', false);
        if (!$lock_acquired) {
            $existing_lock = (int)get_option($lock_key, 0);
            if ($existing_lock > 0 && ($lock_time - $existing_lock) > 10 * MINUTE_IN_SECONDS) {
                delete_option($lock_key);
                $lock_acquired = add_option($lock_key, (string)$lock_time, '', false);
            }
        }
        if (!$lock_acquired) {
            return;
        }

        try {
            self::create_table();
            self::migrate_from_legacy_tables();
            update_option(self::DB_VERSION_OPTION, self::DB_VERSION, false);
        } finally {
            delete_option($lock_key);
        }
    }

    private static function migrate_from_legacy_tables(): void {
        // Legacy table auto-migration intentionally disabled for WordPress.org review compliance.
        // Older session rows are temporary runtime data and can safely expire instead of being
        // copied with dynamically assembled INSERT ... SELECT statements.
        return;
    }

